package Oct.ex_31102024;
public class Lab1 {
    public class Main {
        public static void main(String[] args) {
            for (int i = 0; i < 10; i++) {  // Declare i as an int
                System.out.println(i);
            }

            for (int a = 0; a <= 10; a++) {
                System.out.println(a);
            }

            for (char c = 'a'; c <= 'z'; c++) {  // Use char for character iteration
                System.out.println(c);
            }
        }
    }
}